def stosskurzschlussstrom(x):
    """
    Funktion zur Berechnung des Stosskurzschlussstromes
    """
    i_p = x+x
    return i_p

def thermisch_gleichwertiger_kurzschlussstrom(x):
    """
    Funktion zur Berechnung des thermisch gleichwertigen kurzschlussstromes
    """
    i_th = x^2
    return i_th
